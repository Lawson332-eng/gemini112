/**
 * 获取牌面含义
 * @param {number} cardId - 塔罗牌ID
 * @param {boolean} isReversed - 是否逆位
 * @returns {object} 牌面含义对象
 */
function getCardMeaning(cardId, isReversed = false) {
  const meaning = CARD_MEANINGS[cardId] || CARD_MEANINGS.default;
  return isReversed ? meaning.reversed : meaning.upright;
}

/**
 * 获取指定方面的解读
 * @param {number} cardId - 塔罗牌ID
 * @param {string} aspect - 方面（general/love/career）
 * @param {boolean} isReversed - 是否逆位
 * @returns {string} 具体方面的解读
 */
function getCardAspect(cardId, aspect = 'general', isReversed = false) {
  const meaning = getCardMeaning(cardId, isReversed);
  return meaning[aspect] || meaning.general;
}

